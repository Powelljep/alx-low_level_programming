/**
 * op_add - performs addition
 * @a: first operand
 * @b: second operand
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction
 * @a: first operand
 * @b: second operand
 *
 * Return: subtraction of b from a
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs multiplication on operands
 * @a: first operand
 * @b: second operand
 *
 * Return: multipication of a by b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides operands
 * @a: first operand
 * @b: second operand
 *
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs modulo operation on operands
 * @a: first operand
 * @a: first operand
 *
 * Return: modulus of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
