/**
 * add - adds two integers and returns the result.
 * @a: first value to be added.
 * @b: second value to be added.
 *
 * Description: a function that adds two integers and returns the result.
 * Return: returns the value of adding a and b.
 */
int add(int a, int b)
{
	int result = a + b;
	return (result);
}
